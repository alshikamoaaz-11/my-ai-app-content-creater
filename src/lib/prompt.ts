export type DraftFormInput = {
  category: string;
  partner: string;
  mechanic: string;
  detail: string;
  link: string;
};

function pushContext(lines: string[], context?: string) {
  const clean = context?.trim();
  if (!clean) return;
  lines.push(
    "",
    "سياق الصفحة المستخرج من الرابط (استخدمه كمصدر وحيد للحقائق — لا تخترع خارجه):",
    clean.slice(0, 4000),
    ""
  );
}

export function buildUserMessage(
  input: DraftFormInput,
  context?: string,
  retrievedExamples?: string[]
): string {
  const category = findCategory(input.category);
  const mechanic = findMechanic(input.mechanic);

  const lines = [
    `الفئة: ${category?.label ?? input.category} (${category?.templateHint ?? ""})`,
  ];

  if (input.partner.trim()) {
    lines.push(`اسم الشريك / البراند: ${input.partner.trim()}`);
  }
  if (mechanic && mechanic.value !== "none") {
    lines.push(`آلية المكافأة: ${mechanic.label}`);
  }
  if (input.detail.trim()) {
    lines.push(`تفاصيل / نسبة الخصم: ${input.detail.trim()}`);
  }
  if (input.link.trim()) {
    lines.push(`الرابط الفعلي المطلوب تضمينه في مكان [LINK]: ${input.link.trim()}`);
  } else {
    lines.push("لا يوجد رابط لهذا الموضوع — احذف سطر الرابط بالكامل من المخرج.");
  }

  pushContext(lines, context);

  pushStyleDna(lines);

  // Prefer real examples retrieved from the corpus for this category; fall back
  // to the curated hardcoded examples when retrieval is unavailable/empty.
  const examples =
    retrievedExamples && retrievedExamples.length
      ? retrievedExamples
      : getVoiceExamples(input.category, 3);
  if (examples.length) {
    lines.push("", "أمثلة حقيقية منشورة سابقًا لنفس الفئة (للاسترشاد بالأسلوب فقط):");
    examples.forEach((ex, i) => lines.push(`${i + 1}) ${ex}`));
  }

  lines.push(
    "",
    "اكتب مسودة تغريدة واحدة مطابقة للقالب المناسب أعلاه، بدون أي هاشتاقات داخل نصها."
  );

  return lines.join("\n");
}

/**
 * Chat mode: the marketer describes the topic in free text instead of
 * filling the structured fields. The model must infer the closest template
 * itself, so we sample a couple of real examples across categories rather
 * than one specific bucket.
 */
export function buildChatUserMessage(topic: string, context?: string): string {
  const lines = [
    "الموضوع (وصف حر من المستخدم):",
    topic.trim(),
    "",
    "اختر أنسب قالب من القوالب السبعة بناءً على هذا الموضوع. أخرج نص التغريدة النهائية فقط، بدون ذكر رقم القالب أو أي شرح إضافي، وبدون أي هاشتاقات داخل نصها.",
    "الرابط: إذا ذكر المستخدم رابطًا فعليًا ضمن الموضوع أعلاه، ضعه حرفيًا في مكان [LINK] بالقالب. إذا لم يُذكر أي رابط، احذف سطر الرابط بالكامل من المخرج النهائي — لا تكتب [LINK] كنص حرفي أبدًا.",
  ];

  pushContext(lines, context);

  pushStyleDna(lines);

  lines.push(
    "",
    "أمثلة حقيقية منشورة سابقًا من فئات مختلفة (للاسترشاد بالأسلوب العام فقط، وليست بالضرورة من نفس فئة الموضوع):"
  );

  const sampleCategories = CATEGORIES.map((c) => c.value).slice(0, 4);
  let n = 1;
  for (const cat of sampleCategories) {
    const ex = VOICE_EXAMPLES[cat]?.[0];
    if (ex) {
      lines.push(`${n}) ${ex}`);
      n++;
    }
  }

  lines.push(
    "",
    "اكتب مسودة تغريدة واحدة مطابقة للقالب المناسب، بدون أي هاشتاقات داخل نصها."
  );

  return lines.join("\n");
}
