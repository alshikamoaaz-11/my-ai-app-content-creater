export type DraftFormInput = {
  category: string;
  partner: string;
  mechanic: string;
  detail: string;
  link: string;
};

export function buildUserMessage(input: DraftFormInput): string {
  const category = findCategory(input.category);
  const mechanic = findMechanic(input.mechanic);

  const lines = [
    `الفئة: ${category?.label ?? input.category} (${category?.templateHint ?? ""})`,
  ];

  if (input.partner.trim()) {
    lines.push(`اسم الشريك / البراند: ${input.partner.trim()}`);
  }
  if (mechanic && mechanic.value !== "none") {
    lines.push(`آلية المكافأة: ${mechanic.label}`);
  }
  if (input.detail.trim()) {
    lines.push(`تفاصيل / نسبة الخصم: ${input.detail.trim()}`);
  }
  lines.push(
    "الرابط: اترك النص [LINK] كما هو حرفيًا في مكان الرابط — لا تكتب رابطًا فعليًا."
  );

  const examples = getVoiceExamples(input.category, 3);
  if (examples.length) {
    lines.push("", "أمثلة حقيقية منشورة سابقًا لنفس الفئة (للاسترشاد بالأسلوب فقط):");
    examples.forEach((ex, i) => lines.push(`${i + 1}) ${ex}`));
  }

  lines.push("", "اكتب مسودة تغريدة واحدة مطابقة للقالب المناسب أعلاه.");

  return lines.join("\n");
}

/**
 * Chat mode: the marketer describes the topic in free text instead of
 * filling the structured fields. The model must infer the closest template
 * itself, so we sample a couple of real examples across categories rather
 * than one specific bucket.
 */
export function buildChatUserMessage(topic: string): string {
  const lines = [
    "الموضوع (وصف حر من المستخدم):",
    topic.trim(),
    "",
    "اختر أنسب قالب من القوالب السبعة بناءً على هذا الموضوع. أخرج نص التغريدة النهائية فقط، بدون ذكر رقم القالب أو أي شرح إضافي.",
    "الرابط: اترك النص [LINK] كما هو حرفيًا في مكان الرابط — لا تكتب رابطًا فعليًا.",
    "",
    "أمثلة حقيقية منشورة سابقًا من فئات مختلفة (للاسترشاد بالأسلوب العام فقط، وليست بالضرورة من نفس فئة الموضوع):",
  ];

  const sampleCategories = CATEGORIES.map((c) => c.value).slice(0, 4);
  let n = 1;
  for (const cat of sampleCategories) {
    const ex = VOICE_EXAMPLES[cat]?.[0];
    if (ex) {
      lines.push(`${n}) ${ex}`);
      n++;
    }
  }

  lines.push("", "اكتب مسودة تغريدة واحدة مطابقة للقالب المناسب.");

  return lines.join("\n");
}
