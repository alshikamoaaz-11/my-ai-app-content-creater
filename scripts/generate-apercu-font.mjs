#!/usr/bin/env node
/**
 * Regenerates src/fonts/apercu.generated.ts based on whether the licensed
 * Apercu font files are present in src/fonts/apercu/ (see the README there).
 *
 * next/font/local hard-fails the build if it references a font file that
 * doesn't exist on disk — there is no runtime "try and fall back" for a
 * missing file. So this script decides, at codegen time (before dev/build
 * even starts), whether the generated module calls next/font/local at all:
 *
 *   - All expected files present -> generate a module that self-hosts them.
 *   - Any file missing           -> generate a no-op module (empty exports),
 *                                    so next/font/local is never invoked
 *                                    against a missing file, and CSS falls
 *                                    through to Tahoma (see globals.css).
 *
 * Runs automatically via the `predev`/`prebuild` npm scripts, so dropping
 * font files into src/fonts/apercu/ activates Apercu with zero code changes
 * — just re-run `npm run dev` / `npm run build`.
 */
import { existsSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = join(__dirname, "..");
const fontDir = join(projectRoot, "src/fonts/apercu");
const outFile = join(projectRoot, "src/fonts/apercu.generated.ts");

// Keep in sync with src/fonts/apercu/README.md.
const WEIGHTS = [
  { file: "Apercu-Regular.woff2", weight: "400", style: "normal" },
  { file: "Apercu-Medium.woff2", weight: "500", style: "normal" },
  { file: "Apercu-Bold.woff2", weight: "700", style: "normal" },
];

const present = WEIGHTS.filter((w) => existsSync(join(fontDir, w.file)));
const allPresent = present.length === WEIGHTS.length;

const header =
  "// AUTO-GENERATED by scripts/generate-apercu-font.mjs — do not edit by hand.\n";

let content;
if (allPresent) {
  const srcEntries = WEIGHTS.map(
    (w) =>
      `    { path: "./apercu/${w.file}", weight: "${w.weight}", style: "${w.style}" },`
  ).join("\n");

  content =
    header +
    `// Apercu font files found in src/fonts/apercu/ — self-hosting via next/font/local.
import localFont from "next/font/local";

const apercu = localFont({
  src: [
${srcEntries}
  ],
  variable: "--font-apercu",
  display: "swap",
  fallback: ["Tahoma", "sans-serif"],
});

export const apercuVariable = apercu.variable;
export const apercuClassName = apercu.className;
`;
} else {
  content =
    header +
    `// No Apercu font files found in src/fonts/apercu/ (see README.md there).
// next/font/local is intentionally NOT called here — it would fail the
// build if pointed at a missing file. Typography falls back to Tahoma via
// the CSS stack in src/app/globals.css.
export const apercuVariable = "";
export const apercuClassName = "";
`;
}

writeFileSync(outFile, content);

console.log(
  allPresent
    ? `[fonts] Apercu font files found (${present.length}/${WEIGHTS.length}) — self-hosting active.`
    : `[fonts] Apercu font files not found (${present.length}/${WEIGHTS.length}) in src/fonts/apercu/ — using Tahoma fallback.`
);
